function isHeld(type, id)
    if not id  or id == 0 then return false end
    if HELDS_X_INFO[id] then
        if HELDS_X_INFO[id].type == type then
            return true
        end
    end
    if HELDS_Y_INFO[id] then
        if HELDS_Y_INFO[id].type == type then
            return true
        end
    end
    if HELDS_U_INFO[id] then
        if HELDS_U_INFO[id].type == type then
            return true
        end
    end
    return false
end